    private String languageName;
    private String code;
    private String i18nCode;

    private Language(String languageName, String code, String i18nCode) {
        this.languageName = languageName;
        this.code = code;
        this.i18nCode = i18nCode;
    }

    public String getLanguageName() {
        return languageName;
    }

    public String getCode() {
        return code;
    }

    public String getI18nCode() {
        return i18nCode;
    }

    public void setI18nCode(String i18nCode) {
        this.i18nCode = i18nCode;
    }

    public static Language fromName(String s) {
        for (Language type : values()) {
            if (type.getLanguageName().equals(s)) {
                return type;
            }
        }
        throw new IllegalArgumentException("No Language exists with name '" + s + "'");
    }

    public static Language fromI18NCode(String i18NCode) {
        Language[] languages = Language.values();
        Language found = null;
        for (Language language : languages) {
            if (language.getI18nCode().equals(i18NCode)) {
                found = language;
            }
        }
        return found;
    }

    public static Language fromCode(String code) {
        Language[] languages = Language.values();
        Language found = null;
        for (Language language : languages) {
            if (language.getCode().equals(code)) {
                found = language;
            }
        }
        return found;
    }

    public String toString() {
        return languageName;
    }
